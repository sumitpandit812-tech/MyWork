import math #import math lib

num = int(input("Enter a number: "))
sqrt = math.sqrt(num)
print(f"Square root: {sqrt}")
log = math.log(num)
print(f"Logarithm: {log}")
sine = math.sin(num)
print(f"Sine: {sine}")
cub = math.cbrt(num)
print('---------------------------------' )
print(f"Cube root: {cub}")#new added by me


